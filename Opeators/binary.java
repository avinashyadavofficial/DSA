import java.util.*;
public class binary {
    public static void main(String args[]){
        //Binary operators are type of arithmetic and it needs to 2 operands to perform.
        int a=10;
        int b=20;
        System.out.println("Sum is " + (b+a));
        System.out.println("Difference is " + (b-a));
        System.out.println("Multiply is " + (b*a));
        System.out.println("Divide is " + (b/a));
        System.out.println("Module is " + (b%a));
    }

    
}
