import java.util.*;
public class switch_case {
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        int a=sc.nextInt();
        switch (a) {
            case 1: System.out.println("One");
                
                break;
        
            default: System.out.println("Not one");
                break;
        }
    }
}
